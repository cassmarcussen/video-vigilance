window.onload = function() { createAudioTranscription() };

function createAudioTranscription() {
  console.log('Fetching audio transcription.');
  // Fetch the audio transcription of the passed in video.
  fetch('/audio-transcription', {
    method: 'GET'
  }).then(response => response.text()).then((transcription) => {
    console.log('Fetched audio transcription: ' + transcription);
    const transcriptionObj = JSON.parse(transcription);
    
    // Display transcript of audio.
    const transcriptionElement = document.getElementById('results-transcription');
    transcriptionElement.innerText = '';
    const transcriptionDiv = document.createElement('div');

    const titleElement = document.createElement('p');
    titleElement.innerText = 'The transcription of the video file generated by VI API: ';

    const textElement = document.createElement('p');
    textElement.innerText = transcriptionObj; 

    transcriptionDiv.appendChild(titleElement);
    transcriptionDiv.appendChild(textElement);
    transcriptionElement.appendChild(transcriptionDiv);
  });
}

